const confirmarCurso = () => {
    // Recebe o valor do curso selecionado no input do HTML
    const curso = document.getElementById('curso').value

    // Seleciona a saída da mensagem no html
    const confirmacaoDiv = document.getElementById('confirmacaoCurso')

    // Verifica o curso e exibe a mensagem 
    if (curso === 'sistemasDeInformacao') {
        confirmacaoDiv.innerHTML = '<p>Você está cursando Sistemas de Informação!</p>'
    } else if (curso === 'cienciaDaComputacao') {
        confirmacaoDiv.innerHTML = '<p>Você está cursando Ciência da Computação!</p>'
    } else if (curso === 'engenhariaDaComputacao') {
        confirmacaoDiv.innerHTML = '<p>Você está cursando Engenharia da Computação!</p>'
    } else {
        // Caso não tenha sido selecionado um curso válido
        confirmacaoDiv.innerHTML = '<p>Por favor, selecione um curso válido.</p>'
    }
}


const mostrarDisciplinas = () => {
    //Recebe o curso declarado na entrada do html 
    const curso = document.getElementById('curso').value
    //Recebe o periodo declarado no html, transforma de string para int, e subtrai 1 para ajustar com os indices dos arrays 
    const periodo = parseInt(document.getElementById('periodo').value) - 1

    //Seleciona o local do html onde o resultado será exibido 
    const resultadoDiv = document.getElementById('resultado')

    //Serve para verificar se o usuario selecionol um curso e um periodo valido
    // Se for invalido, o programa exibira na tela o a string declarada no else 
    if (curso && !isNaN(periodo)) {
        
        // Essa função acessa a lista de disciplinas buscando atraves do curso e do periodo declarado na entrada e guarda esse resultado na const lista de disciplinas
        const listaDisciplinas = disciplinas[curso][periodo]

            // Esse if testa se o numero de periodos declarados coincide com o numero de periodos que o curso possui
            // Caso não possua, ele irá exibir a string definida no else 
        if (listaDisciplinas) {

            // Esse trecho define o conteudo da contante resultado.Div, que é o que será exibido na interface html 
            resultadoDiv.innerHTML = `<h2>As disciplinas do ${periodo + 1}º Período são as seguintes:</h2><ul>` + 
                                // A partir do + mapeia a lista das disciplinas. Ela passa elemento por elemento e mapeia cada uma para o elemento '<li>', que significa um elemento de uma lista 
                                // Esse elemento mapeado vai ser guardado dentro do elemento '<ul>' que é uma lista de elementos sem uma numeração definida para cada elemento, mas com marcadores
                                // O elemento .joins('') combina os elementos definidos em <li> para a lista <ul>
                                listaDisciplinas.map(disciplina => `<li>${disciplina}</li>`).join('') +
                                '</ul>'
        } else {
            // Mensagem de erro se o numero de periodos não coincidir com o número de periodos que o curso possui
            resultadoDiv.innerHTML = `<p>O curso selecionado não possui ${periodo + 1} períodos.</p>`
        }
    } else {
        // Mensagem de erro mostrada quando um dos elementos não é selecionado
        resultadoDiv.innerHTML = '<p>Por favor, selecione um curso e um período.</p>'
    }
} 

